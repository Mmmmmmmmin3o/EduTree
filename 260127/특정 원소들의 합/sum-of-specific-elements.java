import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        // Please write your code here.
        Scanner sc=new Scanner(System.in);
        int[][] arr=new int[4][4];
        int answer=0;

        for(int i=0; i<4; i++){
            for(int j=0; j<4; j++){
                arr[i][j]=sc.nextInt();
            }
        }

        for(int i=0; i<4; i++){
            for(int j=0; j<i+1; j++){
                answer+= arr[i][j];
            }
        }
        System.out.println(answer);
    }
}